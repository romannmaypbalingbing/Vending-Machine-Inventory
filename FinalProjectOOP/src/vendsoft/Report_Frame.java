   private void selectDate() {
    // create a File object for "sale.txt"
    File file = new File("sale.txt");

    try {
        // initialize a Scanner to read the file
        Scanner scan = new Scanner(file);

        // a map to store unique dates to avoid duplicates in the JComboBox
        Map<String, Boolean> uniqueDates = new HashMap<>();

        // read each line from the file
        while (scan.hasNext()) {
            String line = scan.nextLine();
            String[] temp = line.split("\t");

            // concatenate day, month, and year to form a date in the format "dd/MM/yyyy"
            String concatenatedDate = temp[0] + "/" + temp[1] + "/" + temp[2];

            // check if the date is not already in the map, then add it to the JComboBox
            if (!uniqueDates.containsKey(concatenatedDate)) {
                jComboBox1.addItem(concatenatedDate);
                uniqueDates.put(concatenatedDate, true);
            }
        }

    } catch (FileNotFoundException ex) {
        // handle the exception if the file is not found
        Logger.getLogger(Report_Frame.class.getName()).log(Level.SEVERE, null, ex);
    }
}
